choice1 = input("You're accross the road. Where do you want to go? left or right?\n").lower()

if choice1 == "left":
    choice2 = input("Do you want to swim or wait?\n").lower()
    if choice2 == "wait":
        choice3 = input("Which door?\n").lower()
        if choice3 == "red":
            print("Fire! Game over")
        elif choice3 == "yellow":
            print("You've found the treasure")
        elif choice3 == "blue":
            print("You fell into the sea. Game over")
        else: 
            print("You didn't choose any door. Game over")
    else:
        print("Game Over")

